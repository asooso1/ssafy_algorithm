# 연습문제1 : 0과 1로 이루어진 1차 배열에서 7개 byte를 묶어서 10진수로 출력하기

data = "0000000111100000011000000111100110000110000111100111100111111001100111"

arr = list(map(int, data))

for i in range(len(arr)//7):
    n = 0
    for j in range(i*7, i*7+7):
        n = n * 2 + arr[j]
    print(n, end=' ')

