# 0111011(7) 0110001(5) 0111011(7) 0110001(5) 0110001(5) 0001101(0) 0010011(2) 0111011(7)
# 7 5 7 5 5 0 2 고유번호
# 7 검증코드
code = '01110110110001011101101100010110001000110100100110111011' # <- 탐색해서 56개 슬라이싱
amho = {  # 딕셔너리
    '0001101': 0,
    '0011001': 1,
    '0010011': 2,
    '0111101': 3,
    '0100011': 4,
    '0110001': 5,
    '0101111': 6,
    '0111011': 7,
    '0110111': 8,
    '0001011': 9
}
a = 0
lst = []
while a < 56 :
    # code 7개씩 슬라이싱
    key = code[a + 0 : a + 7]
    lst.append(amho[key])
    a += 7
# 검증하기 ,앞의 7자리 중에서

print(lst)
