# 0000000111100000011000000111100110000110000111100111100111111001100111

num = '0000000111100000011000000111100110000110000111100111100111111001100111'

ans = []

while num != '':
    num10 = 0
    for i in range(0, 7):
        num10 += 2**(6-i) * int(num[0:7][i])
    ans.append(num10)
    num = num[7:]

print(ans)